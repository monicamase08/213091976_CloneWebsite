		<h1>Our Story</h1>
			<p>Brothers for All is a spin out of Mothers for All, a non-profit social enterprise that was started in 2008 to provide income generation and entrepreneurship support and training to caregivers of vulnerable children in South Africa and Botswana, where our our work has been supported by Barclays Bank, The World Bank and the EU.</p>
			<p>In 2010, Mothers for All started working with an award-winning prisoner initiative, called the Group of Hope, which enabled prisoners to change their lives by helping orphans in nearby impoverished communities. We began by designing delivering peer education programmes to cover HIV, STIs and TB in four different prisons. The work was highly successful with our results presented at X1X International AIDS Conference in Washington in 2012 and the Durban AIDS Conference in 2013. We also started an income generation project in the prison which has generated more than R100,000 to support local communities.</p>
			<p>While working with prisoners trying to turn their lives around, it became clear that aspirational technology skills are the only scalable way to end the cycle of poverty and crime. So in 2014 Brothers for All focused on equipping offenders, ex-offenders and vulnerable youth with aspirational technology and entrepreneurship skills.</p>
