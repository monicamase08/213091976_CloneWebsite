                            <h1>Community Upliftment</h1>
                            <p>Brothers for All members are all expected to help build and strengthen their communities in exchange for job skills training. Once they are enrolled on the coding programme they automatically become part of our Playing it Safe peer education project as well as our income generation programme.</p>
                            <p>The latter helps to generate funds for our upliftment initiatives that focus on: orphans and vulnerable children; advocacy and awareness events around gender violence, substance abuse and crime; and constructive engagement with other non-profits, community initiatives and government departments (such as the Departments of Health, Social Development and Community Corrections and the South African Police Service).</p>
